resumo = "Paloma é uma mulher de 46 anos que deseja mudar de profissão, por isso está estudando 'python'."
idade = 46

# 1. Imprima na tela a variável "resumo"
print(resumo)

# 2. Imprima na tela apenas a segunda letra da variável
print(resumo[1])

# 3. Imprima na tela a idade de Paloma (resposta esperada: "46")
