# 4. Imprima na tela o trecho final da variável
print(resumo[-10:])

# 5. Converta todas as letras para minúsculo e imprima na tela
print(resumo.lower())

# 6. Converta todas as letras para maiúscula e imprima na tela
print(resumo.upper())

# 7. Formate a frase para que a primeira letra de cada palavra seja maiúscula e imprima na tela
